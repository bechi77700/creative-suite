'use client';

import { useEffect, useState } from 'react';
import MultiImageInput, { RefImage } from './MultiImageInput';

export const IMAGE_MODELS = [
  { value: 'nano-banana-pro', label: 'Nano Banana Pro (best — uses ref image if provided)', needsRef: false, allowsRef: true },
  { value: 'nano-banana-2', label: 'Nano Banana 2 (fast, uses ref image if provided)', needsRef: false, allowsRef: true },
  { value: 'nano-banana', label: 'Nano Banana (original, uses ref image if provided)', needsRef: false, allowsRef: true },
  { value: 'flux-pro-ultra', label: 'Flux Pro Ultra (highest realism, no ref)', needsRef: false, allowsRef: false },
  { value: 'recraft-v3', label: 'Recraft v3 (best for ads with text)', needsRef: false, allowsRef: false },
  { value: 'imagen4', label: 'Imagen 4 (Google, photoreal)', needsRef: false, allowsRef: false },
];

interface Props {
  prompt: string;
  /** Pre-loaded reference image(s) provided from the parent form. */
  initialImage?: { base64: string; mimeType: string; previewDataUri: string };
  initialImages?: RefImage[];
  /** Model selected in the main form, used as default */
  initialModel?: string;
  /** When provided, the image is shown immediately (already generated by parent) */
  autoGenerateImageUrl?: string;
}

export default function PromptImageGenerator({
  prompt,
  initialImage,
  initialImages,
  initialModel,
  autoGenerateImageUrl,
}: Props) {
  const seedRefs: RefImage[] =
    initialImages && initialImages.length > 0
      ? initialImages
      : initialImage
      ? [initialImage]
      : [];

  const [model, setModel] = useState(initialModel || 'nano-banana');
  const [refs, setRefs] = useState<RefImage[]>(seedRefs);
  const [feedback, setFeedback] = useState('');
  const [loading, setLoading] = useState(false);
  const [imageUrl, setImageUrl] = useState(autoGenerateImageUrl || '');
  const [error, setError] = useState('');
  const [isFullscreen, setIsFullscreen] = useState(false);

  // Close fullscreen on Escape
  useEffect(() => {
    if (!isFullscreen) return;
    const onKey = (e: KeyboardEvent) => {
      if (e.key === 'Escape') setIsFullscreen(false);
    };
    window.addEventListener('keydown', onKey);
    // Lock body scroll while modal open
    const prev = document.body.style.overflow;
    document.body.style.overflow = 'hidden';
    return () => {
      window.removeEventListener('keydown', onKey);
      document.body.style.overflow = prev;
    };
  }, [isFullscreen]);

  const currentModel = IMAGE_MODELS.find((m) => m.value === model)!;

  const generate = async (withFeedback = false) => {
    if (currentModel.needsRef && refs.length === 0) {
      setError('This model requires a reference image — please upload one above.');
      return;
    }

    setLoading(true);
    setError('');

    const sendRefs = currentModel.allowsRef
      ? refs.map((r) => ({ base64: r.base64, mimeType: r.mimeType }))
      : [];

    try {
      const res = await fetch('/api/generate/image', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          prompt,
          model,
          referenceImages: sendRefs.length > 0 ? sendRefs : undefined,
          feedback: withFeedback ? feedback : undefined,
        }),
      });

      if (!res.ok) {
        const err = await res.json().catch(() => ({ error: `Server error ${res.status}` }));
        setError(err.error || `Error ${res.status}`);
        setLoading(false);
        return;
      }

      const data = await res.json();
      setImageUrl(data.imageUrl);
      if (withFeedback) setFeedback('');
    } catch (e) {
      setError(e instanceof Error ? e.message : 'Unexpected error');
    }

    setLoading(false);
  };

  const downloadImage = async () => {
    if (!imageUrl) return;
    try {
      const res = await fetch(imageUrl);
      const blob = await res.blob();
      const url = URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;
      a.download = `generated-${Date.now()}.png`;
      a.click();
      URL.revokeObjectURL(url);
    } catch {
      // fallback: open in new tab
      window.open(imageUrl, '_blank');
    }
  };

  return (
    <div className="border border-bg-border rounded-lg p-4 mt-3 bg-bg-base/50 space-y-3">
      <div className="flex items-center justify-between">
        <span className="text-text-secondary text-xs uppercase tracking-widest">Generate image with AI</span>
      </div>

      {/* Model selector */}
      <div>
        <label className="text-text-muted text-[10px] uppercase tracking-widest block mb-1">Model</label>
        <select
          className="input-field text-xs"
          value={model}
          onChange={(e) => {
            setModel(e.target.value);
            setImageUrl('');
            setError('');
          }}
        >
          {IMAGE_MODELS.map((m) => (
            <option key={m.value} value={m.value}>{m.label}</option>
          ))}
        </select>
      </div>

      {/* Reference image(s) — only if model supports it */}
      {currentModel.allowsRef && (
        <div>
          <label className="text-text-muted text-[10px] uppercase tracking-widest block mb-1">
            Product reference photo(s) {currentModel.needsRef ? '*' : '(optional — multiple allowed)'}
          </label>
          <MultiImageInput
            images={refs}
            onChange={setRefs}
            compact
            emptyLabel="↑ Upload product photo(s)"
          />
        </div>
      )}

      {/* Generate button (only if no image yet) */}
      {!imageUrl && !loading && (
        <button
          onClick={() => generate(false)}
          className="btn-primary w-full text-xs"
          disabled={loading || (currentModel.needsRef && refs.length === 0)}
        >
          Generate image
        </button>
      )}

      {/* Loading */}
      {loading && (
        <div className="flex items-center justify-center py-8">
          <div className="w-6 h-6 border-2 border-accent-gold/30 border-t-accent-gold rounded-full animate-spin" />
          <span className="text-text-muted text-xs ml-3">Generating image…</span>
        </div>
      )}

      {/* Error */}
      {error && !loading && (
        <div className="border border-accent-red/40 bg-accent-red/5 rounded px-3 py-2 text-xs text-accent-red">
          {error}
        </div>
      )}

      {/* Generated image + feedback/regenerate */}
      {imageUrl && !loading && (
        <div className="space-y-3">
          {/* eslint-disable-next-line @next/next/no-img-element */}
          <img
            src={imageUrl}
            alt="generated"
            onClick={() => setIsFullscreen(true)}
            className="max-w-xs max-h-80 w-auto h-auto rounded-md border border-bg-border mx-auto block cursor-zoom-in hover:opacity-90 transition-opacity"
            title="Click to view full size"
          />

          <div className="flex gap-2">
            <button onClick={downloadImage} className="btn-secondary text-xs flex-1">
              Download
            </button>
            <button
              onClick={() => { setImageUrl(''); setError(''); }}
              className="btn-secondary text-xs flex-1"
            >
              Reset
            </button>
          </div>

          <div>
            <label className="text-text-muted text-[10px] uppercase tracking-widest block mb-1">
              What's wrong? (optional — improves regeneration)
            </label>
            <textarea
              className="input-field resize-none text-xs"
              rows={2}
              placeholder="e.g. text is unreadable, wrong product color, too dark…"
              value={feedback}
              onChange={(e) => setFeedback(e.target.value)}
            />
          </div>

          <button onClick={() => generate(true)} className="btn-primary w-full text-xs">
            Regenerate {feedback.trim() ? 'with feedback' : ''}
          </button>
        </div>
      )}

      {/* Fullscreen lightbox */}
      {isFullscreen && imageUrl && (
        <div
          onClick={() => setIsFullscreen(false)}
          className="fixed inset-0 z-50 bg-black/90 flex items-center justify-center p-6 cursor-zoom-out"
        >
          {/* eslint-disable-next-line @next/next/no-img-element */}
          <img
            src={imageUrl}
            alt="generated full size"
            onClick={(e) => e.stopPropagation()}
            className="max-w-full max-h-full object-contain rounded-md cursor-default"
          />
          <button
            onClick={() => setIsFullscreen(false)}
            className="absolute top-4 right-4 text-white/80 hover:text-white text-2xl w-10 h-10 flex items-center justify-center rounded-full bg-black/50 hover:bg-black/70 transition-colors"
            title="Close (Esc)"
          >
            ✕
          </button>
        </div>
      )}
    </div>
  );
}
